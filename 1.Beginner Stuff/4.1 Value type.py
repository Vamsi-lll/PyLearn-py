# ============================================
# Value Type - Data Types in Python
# ============================================

# As the name itself suggests, Value Type data types
# are used to store a single value at a time.

# From the previous class, we learned that
# Value Types are divided into two categories:
# 1. Number Type
# 2. Non-Number Type


# ============================================
# 1. Number Type
# ============================================

# What is Number Type?
# Data that contains only numbers is called Number Type.
# In Python, Number Types are further classified into:
# - int
# - float
# - complex


# -------- int type --------
# int (integer) type is used to store whole numbers.
# These numbers can be positive or negative.
# It belongs to the class <class 'int'>

num1 = 1
num2 = -1


# -------- float type --------
# float type is used when working with decimal values.
# It belongs to the class <class 'float'>

num3 = 1.2
num4 = -2.4


# -------- complex type --------
# complex type is mainly used for mathematical
# and scientific calculations.
# It belongs to the class <class 'complex'>
# A complex number has a real part and an imaginary part.
# In Python, the imaginary part is represented using 'j'

comp = 3 + 4j


# ============================================
# 2. Non-Number Type
# ============================================

# As the name suggests, Non-Number Types do not
# store numeric values.
# Instead, they store reserved values used for
# logic or initialization.


# -------- Boolean type --------
# Boolean type is used for conditional checking.
# It belongs to the class <class 'bool'>
# It can have only two values: True or False

is_a_developer = True
is_login = False


# -------- None type --------
# None type represents the absence of a value.
# It belongs to the class <class 'NoneType'>
# It is commonly used to initialize a variable
# without assigning a value immediately.

final_value = None
